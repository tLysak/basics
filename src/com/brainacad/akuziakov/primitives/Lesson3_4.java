package com.brainacad.akuziakov.primitives;

public class Lesson3_4 {
    public static void main(String[] args) {
        int intValue = 7;

        System.out.println("intValue is: " + intValue);
        System.out.println("intValue++ is: " + intValue++);
        System.out.println("++intValue is: " + ++intValue);

        System.out.println("--intValue is: " + --intValue);
        System.out.println("intValue-- is: " + intValue--);
        System.out.println("Now intValue is: " + intValue);
    }
}

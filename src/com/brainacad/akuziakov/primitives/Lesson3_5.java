package com.brainacad.akuziakov.primitives;

public class Lesson3_5 {
    public static void main(String[] args) {
        int intValue = -777;

        System.out.println("Initial value of intValue is: " + intValue);
        System.out.println("The result of intValue>>3: " + (intValue >> 3));
        System.out.println("The result of intValue<<3: " + (intValue << 3));
    }
}

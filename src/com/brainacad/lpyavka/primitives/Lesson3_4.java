package com.brainacad.lpyavka.primitives;

public class Lesson3_4 {
    public static void main(String[] args) {
        int value = 15;
        System.out.println("Initial value is " + value);

        System.out.println("Pre increment value is " + ++value);
        System.out.println("Post increment value is " + value++);
        System.out.println("Final increment value is " + value);
        System.out.println("Pre decrement value is " + --value);
        System.out.println("Post decrement value is " + value--);
        System.out.println("Final decrement value is " + value);
    }
}

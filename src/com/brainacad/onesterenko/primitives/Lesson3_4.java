package com.brainacad.onesterenko.primitives;

public class Lesson3_4 {
    public static void main (String [] args){
        int a = 4;

        System.out.println("Postfix increment result is "+ a++);
        System.out.println("Postfix decrement result is "+ a--);
        System.out.println("Prefix decrement result is "+ --a);
        System.out.println("Prefix increment result is "+ ++a);
    }
}

package com.brainacad.vmarch.primitives;

public class Lesson3_3 {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        System.out.println("AND = " + (a & b));
        System.out.println("OR = " + (a | b));
        System.out.println("NOT = " + (a != b));
        System.out.println("XOR = " + (a ^ b));
    }
}

package com.brainacad.vmarch.primitives;

public class Lesson3_6 {
    public static void main(String[] args) {
        int a = -4, b = 2;
        System.out.println("& = " + (a & b));
        System.out.println("| = " + (a | b));
        System.out.println("^ = " + (a ^ b));
        System.out.println("~ = " + ~a);

    }
}

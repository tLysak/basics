package com.brainacad.skozigon.primitives;


public class Lesson3_1 {
    public static void main(String[] args) {
        byte b = 124;
        short s = 077777;
        int i ='H';
        long l =9223332465465264823l;
        double d = 2.35e15;
        char c = '\u0548';
        boolean bool = true;
        System.out.println("Byte variable is: " + b);
        System.out.println("Short variable is: " + s );
        System.out.println("Integer variable is: " + i );
        System.out.println("Long variable is: " + l );
        System.out.println("Double variable is: " + d );
        System.out.println("Char variable is:" + c);
        System.out.println("Boolean variable is: " + bool);
    }
}

package com.brainacad.skozigon.primitives;


public class Lesson3_4 {
    public static void main(String[] args) {
        int i = 100;
        System.out.println("Integer variable i = " + i);
        System.out.println("In prefix increment position ++i result: " + (++i) + "\ti = " + i);
        System.out.println("Integer variable i = " + i);
        System.out.println("In prefix decrement position --i result: " + (--i) + "\ti = " + i);
        System.out.println("Integer variable i = " + i);
        System.out.println("In postfix increment position i++ result: " + (i++) + "\ti = " + i);
        System.out.println("Integer variable i = " + i);
        System.out.println("In postfix decrement position i-- result: " + (i--) + "\ti = " + i);
    }
}

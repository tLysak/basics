package com.brainacad.vmarch.lesson6.mymath;


public class MyMath {


    static double areaOfCircle(double radius) {
        return Math.PI * (radius * radius);
    }


    static int findMax(int array[]) {

        return array[array.length - 1];
    }

    static int findMin(int array[]) {

        return array[0];
    }


}



